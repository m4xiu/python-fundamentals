# What is variable?
# we use variables to temporarily store data in memory.
# we can use variables to store any type of data, such as numbers, strings, lists, etc.

# storing data in variables
name = 'Mathew'  # this is a string variable
age = 19  # this is an integer variable
height = 168.5  # this is a float variable
print('Name:', name)
print('Age:', age)
print('Height:', height)
print("-" * 20)
# changing the value of a variable
age = 20
print('Updated Age:', age)
print("-" * 20)
# multiple assignment
x, y, z = 10, 20, 30
print(x, y, z)
print("-" * 20)
# assigning the same value to multiple variables
a = b = c = 50
print(a, b, c)
print("-" * 20)
# Taking input from the user and storing it in a variable
fav_language = input('What is your favorite programming language? ')
print('Your favorite programming language is:', fav_language)
print("-" * 20)

# variable naming rules
# 1. Variable names must start with a letter or an underscore (_).
# 2. Variable names can only contain letters, numbers, and underscores.
# 3. Variable names are case-sensitive (e.g., age and Age are different variables).
# 4. Variable names cannot be the same as Python keywords (e.g., if, else, for, while, etc.).

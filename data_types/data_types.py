# Data Types in Python
# 1. int: Represents whole numbers, both positive and negative.
# 2. float: Represents decimal numbers.
# 3. str: Represents sequences of characters, enclosed in single or double quotes.
# 4. bool: Represents Boolean values, which can be either True or False.
# 5. list: Represents an ordered collection of items, which can be of different data
#    types. Lists are mutable, meaning they can be modified after creation.
# 6. tuple: Represents an ordered collection of items, similar to lists, but tuples
#    are immutable, meaning they cannot be modified after creation.
# 7. dict: Represents a collection of key-value pairs, where each key is unique
#    and maps to a value. Dictionaries are mutable.
# 8. set: Represents an unordered collection of unique items. Sets are mutable and
#    do not allow duplicate values.
# 9. NoneType: Represents the absence of a value or a null value. It is represented by the keyword None.

# Example usage of data types in Python
# Integer
a = 10
print(type(a))  # Output: <class 'int'>
# Float
b = 3.14
print(type(b))  # Output: <class 'float'>
# String
c = "Hello, World!"
print(type(c))  # Output: <class 'str'>
# Boolean
d = True
print(type(d))  # Output: <class 'bool'>
# List
e = [1, 2, 3]
print(type(e))  # Output: <class 'list'>
# Tuple
f = (1, 2, 3)
print(type(f))  # Output: <class 'tuple'>
# Dictionary
g = {"name": "Alice", "age": 30}
print(type(g))  # Output: <class 'dict'>
# Set
h = {1, 2, 3}
print(type(h))  # Output: <class 'set'>
# NoneType
i = None
print(type(i))  # Output: <class 'NoneType'>
